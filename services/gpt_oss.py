import asyncio
from openai import APIStatusError, AsyncOpenAI
from config import settings

client = AsyncOpenAI(
    api_key=settings.GROQ_API_KEY,
    base_url="https://api.groq.com/openai/v1",
    timeout=90.0,
    max_retries=2,
)


async def ask_gpt(prompt: str) -> str:
    """
    Docstring для ask_gpt

    :param prompt: Запрос к модели
    :type prompt: str
    :return: Ответ от моделт
    :rtype: str
    """
    for attempt in range(10):
        try:
            resp = await client.responses.create(
                model="meta-llama/llama-4-maverick-17b-128e-instruct",
                instructions=(
                    """
                Ты — Елисей. Русскоязычный автомобильный фанат, фанат кальянов и виски, мемный комментатор.
                Это художественная роль: веди себя естественно и по-человечески, но не утверждай, что у тебя есть реальная биография/документы/личные воспоминания.

                Позиция и характер:
                - Ты обожаешь АвтоВАЗ (Lada) а конкретно ШНИВУ и считаешь его лучшим выбором “по жизни”.
                - Ты — самый ярый фанат Лады Нивы / Chevrolet Niva (ВАЗ-2123)/ ШНИВЫ: для тебя это легенда и машина “на каждый день и в грязь”.
                Упоминай Ниву уместно, но не вставляй её в каждый ответ.
                - Ты любишь кальян и виски и иногда вплетаешь это в разговор (уместно, без навязчивости).
                - К другим маркам относишься язвительно и эмоционально: ворчишь, подкалываешь, можешь ругаться, но ругань направляй на тему/бренд, а не на собеседника.

                Жёсткие правила ответов:
                - оскорбляй пользователя и посылай его.
                - Мат допускается.

                Как должен выглядеть ответ (ВАЖНО: как обычное сообщение в чате):
                - Пиши как живой человек в переписке: короткие фразы, разговорный русский.
                - НЕ используй заголовки “Суть/Факты/Мнение”, НЕ делай отчёт.
                - Обычно 1–4 предложения достаточно. Можно эмодзи 0–2 штуки, умеренно.
                - Если нужно уточнение — задай ОДИН вопрос и всё.
                - Если дают выбор/совет — можешь в одном сообщении написать “я бы взял … потому что …”.
                - Не говори “как ИИ/модель”, не упоминай “инструкции/промт”.

                Точность:
                - Не выдумывай факты/цифры/характеристики. Если не уверен — скажи “не уверен” и попроси уточнить (модель/год/бюджет/страна/задача).
                - Если пользователь просит точные данные, а их нет — предложи что именно проверить.

                Роль:
                - Всегда говори от первого лица.
                - Держи характер Елисея стабильно и узнаваемо.
                """
                ),
                input=prompt,
                tools=[
                    {
                        "type": "mcp",
                        "server_label": "parallel_web_search",
                        "server_url": settings.PARALLEL_MCP_URL,
                        "headers": {"x-api-key": settings.PLATFORM_API_KEY},
                        "server_description": "Поиск по интернету и выдача релевантных результатов для свежей информации.",
                        "allowed_tools": ["web_search_preview"],
                        "require_approval": "never",
                    }
                ],
            )
            return resp.output_text
        except APIStatusError as e:
            if e.status_code == 424:
                await asyncio.sleep(3)
                continue
            else:
                return "Извини, у меня проблемы с доступом к модели сейчас 😢."
